export const generateRoomData = (roomTypeArray) => {
  let roomTypeArrayWithId = [];
  let roomArray = [];
  let bookingArray = [];

  for (let i = 0; i < roomTypeArray.length; i++) {
    let roomType = {
      name: roomTypeArray[i].name,
      quantity: roomTypeArray[i].quantity,
      rate: roomTypeArray[i].rate,
      roomTypeId: `rt${i}`,
    };
    roomTypeArrayWithId.push(roomType);
    let roomObject = { roomTypeId: `rt${i}`, rooms: [] };
    for (let j = 1; j <= roomTypeArray[i].quantity; j++) {
      let individualRoom = {
        roomTypeId: `rt${i}`,
        roomId: `rt${i}-${j}`,
        occupied: false,
        maintenance: Math.random() > 0.6 ? true : false,
        extraInfo: {},
        roomNumber: j,
      };
      if (Math.random() > 0.3) {
        const randomBookingNumber = Math.floor(Math.random() * 5);
        for (let k = 0; k < randomBookingNumber; k++) {
          const randomMonth = Math.round(Math.random() * 12);
          const firstDate = dayjs()
            .set("month", randomMonth)
            .set("date", Math.floor(Math.random() * 28));
          const seconDate = dayjs()
            .set("month", randomMonth)
            .set("date", Math.floor(Math.random() * 28));
          let dateArray = ["", ""];
          if (firstDate.isBefore(seconDate)) {
            dateArray[0] = firstDate;
            dateArray[1] = seconDate;
          } else {
            dateArray[1] = firstDate;
            dateArray[0] = seconDate;
          }

          if (dateArray[0].isBefore(dayjs()) && dateArray[1].isAfter(dayjs())) {
            individualRoom.occupied = true;
            individualRoom.maintenance = false;
          }
          let randomNameIndex = Math.floor(Math.random() * 100);
          // setting all the details for the booking
          let booking = {
            roomId: `rt${i}-${j}`,
            roomNumber: j,
            bookingId: `rt${i}-${j}-r${bookingArray.length}`,
            roomTypeName: roomTypeArray[i].name,
            roomTypeId: `rt${i}`,
            firstName: demoFirstNameArray[randomNameIndex],
            lastName: demoLastNameArray[randomNameIndex],
            email: `${demoFirstNameArray[
              randomNameIndex
            ].toLowerCase()}_${demoLastNameArray[
              randomNameIndex
            ].toLowerCase()}@gmail.com`,
            phone: `+${Math.floor(Math.random() * 10000000000)}`,
            country: "",
            city: "",
            street: "",
            streeNumber: "",
            nationality:
              countryList[Math.floor(Math.random() * countryList.length)],
            passportNumber: Math.floor(Math.random() * 100000000),
            age: 18 + Math.floor(Math.random() * 70),
            gender: Math.random() > 0.5 ? "M" : "F",
            specialRequirements: "",

            checkInDate: dateArray[0],
            checkOutDate: dateArray[1],
            duration: dateArray[1].diff(dateArray[0], "day"),
            rate: roomTypeArray[i].rate,
            totalPrice:
              roomTypeArray[i].rate * dateArray[1].diff(dateArray[0], "day"),
            extraInfo: {},
          };
          bookingArray.push(booking);
        }
      }

      roomObject.rooms.push(individualRoom);
    }
    roomArray.push(roomObject);
  }
  return [roomTypeArrayWithId, roomArray, bookingArray];
};

export const [roomTypeArrayWithId, roomArray, bookingArray] =
  generateRoomData(demoDataRoomArray);

const roomTypeSlice = createSlice({
  name: "Room Type Slice",
  initialState: {
    roomTypes: roomTypeArrayWithId,
  },
  reducers: {
    changeRate(state, action) {
      const { roomTypeId, newRate } = action.payload;
      const searchedRoomType = state.roomTypes.find(
        (item) => item.roomTypeId === roomTypeId
      );
      searchedRoomType.rate = newRate;
    },
  },
});

export default roomTypeSlice;
export const roomTypeActions = roomTypeSlice.actions;
